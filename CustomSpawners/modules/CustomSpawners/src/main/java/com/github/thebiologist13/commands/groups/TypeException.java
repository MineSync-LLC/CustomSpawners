package com.github.thebiologist13.commands.groups;

public class TypeException extends Exception {

	private static final long serialVersionUID = -5953239848920183767L;

	public TypeException() {super();}

	public TypeException(String arg0) {super(arg0);}

	public TypeException(Throwable arg0) {super(arg0);}

	public TypeException(String arg0, Throwable arg1) {super(arg0, arg1);}

}
